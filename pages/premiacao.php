        <h2>PREMIAÇÃO</h2>
        <h3>Destaque valores positivos, que possam incentivar outras mulheres.</h3> 
        
        <p>O Prêmio SEBRAE Mulher de Negócios é composto por 2 (duas) etapas: estadual e nacional.</p>

        <h4>Etapa Estadual</h4>
        <p>Nesta etapa, podem ser reconhecidas como vencedoras estaduais até 3 (três) empreendedoras de cada estado e 3 (três) do Distrito Federal; uma para cada categoria prevista, podendo-se chegar a até 81 empreendedoras.</p>
        <p>Todas Unidades da federação do SEBRAE, oferecerão as suas vencedoras:</p>
        <ul>
            <li>Uma placa/ troféu de reconhecimento; </li>
            <li>Certificado de premiação;</li>
            <li>Direito ao uso do selo de vencedora, explicitando o ciclo;</li>
            <li>Um curso da Matriz de Soluções Educacionais e/ou 16 (dezesseis) horas técnicas de consultoria em gestão.</li>
        </ul>


        <h4>Etapa Nacional</h4>
        <p>Nesta etapa, podem ser reconhecidas como vencedoras estaduais até 3 (três) empreendedoras de cada estado e 3 (três) do Distrito Federal; uma para cada categoria prevista, podendo-se chegar a até 81 empreendedoras.</p>
        <p>Todas Unidades da federação do SEBRAE, oferecerão as suas vencedoras:</p>
        <ul>
            <li>Uma placa/ troféu de reconhecimento; </li>
            <li>Certificado de premiação;</li>
            <li>Direito ao uso do selo de vencedora, explicitando o ciclo;</li>
            <li>Um curso da Matriz de Soluções Educacionais e/ou 16 (dezesseis) horas técnicas de consultoria em gestão.</li>
        </ul>
        
